    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(user != null && password != null){
            if (verificarCredenciales(user.getText())) {
            JOptionPane.showMessageDialog(null,"Usuario existente, vuelva a intentarlo");
            limpiarCampos();
        } else if(agregarUsuario(user.getText(), password.getText(), nombre.getText(), paterno.getText(), materno.getText())) {
            JOptionPane.showMessageDialog(null, "Usuario registrado correctamente");
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Error en el registro de usuario");
        }
        }else{
            JOptionPane.showMessageDialog(null, "❌ Falta teclear usuario y/o contrasenia");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

