    // Método para obtener conexión
    public static Connection getConnection() {
        Connection connection = null;
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            connection = DriverManager.getConnection(URL, USER, PASSWORD);
        } catch (ClassNotFoundException | SQLException e) {
            e.printStackTrace();
        }
        return connection;
    }
    public static boolean verificarCredenciales(String usuario) {
        String sql = "SELECT * FROM usuarios WHERE Usuario = ?";
        
        try (Connection conn = getConnection();
             PreparedStatement pstmt = conn.prepareStatement(sql)) {
             
            pstmt.setString(1, usuario);

            try (ResultSet rs = pstmt.executeQuery()) {
                return rs.next(); // Si hay un resultado, las credenciales son correctas
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return false;
    }
    
    public static boolean agregarUsuario(String usuario, String contrasenia, String nombre, String paterno,String materno) {
    String sql = "INSERT INTO usuarios (Usuario, Contraseña, Nombre, Paterno, Materno) VALUES (?, ?, ?, ?, ?)";

    try (Connection conn = getConnection();
         PreparedStatement pstmt = conn.prepareStatement(sql)) {

        pstmt.setString(1, usuario);
        pstmt.setString(2, contrasenia);
        pstmt.setString(3, nombre);
        pstmt.setString(4, paterno);
        pstmt.setString(5, materno);

        int filasAfectadas = pstmt.executeUpdate();
        return filasAfectadas > 0; // Retorna true si se insertó correctamente
    } catch (SQLException e) {
        e.printStackTrace();
    }
    return false;
}


